package arrays;

public class multidimensional_stringarray1 {

	public static void main(String[] args) {
		
		
	String [] [] s= {{"akshay","ashish","jaydip"},{"tanaji","pratik","vishal"},{"rahul","akash","yash"}	};

	for(int i=0;i<=2;i++) 
	{
		for(int j=0;j<=2;j++) 
		{
			System.out.print(s[i][j]+" ");
		}
		System.out.println();
	}
	
	
	
	
	}	
}
