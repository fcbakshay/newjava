package arrays;

public class intarrays {

	public static void main(String[] args) {

		int[] ar = new int[5]; // array declaration

		ar[0] = 100;
		ar[1] = 200;
		ar[2] = 300; // array initialization
		ar[3] = 400;
		ar[4] = 500;

		for(int i = 0; i <= 4; i++) { // array usage
			System.out.println(ar[i]);
		}

	}

}
